  <h1 style="text-align: center;">About Us</h1>
  <p>Adikanfo Commodities Ltd (ACL) is a limited liability company registered under the Ghana Companies Code 1963 (Act 179) and obtained operational license to purchase cocoa in Ghana on October 6, 2016, under Ghana Cocoa Board Law 1984 (P.N.D.C Law 81 S.4(6)).</p>
  <p>The company is authorized to deal in the purchase of cocoa. The core business of the company is marketing, handling, packaging and warehousing of cocoa.</p>
  <p>The Company started operating on a small scale but now has extended its operations and currently operates in six (6) out of the seven (7) cocoa regions in Ghana with the exception of Volta Region.</p>
  <p>Adikanfo Commodities Ltd is a registered member of WORLD COCOA FOUNDATION (WCF) and LICENSED COCOA BUYERS ASSOCIATION OF GHANA (LICOBAG). The Managing Director Mr Theophilus Agyare Asare serves on the executive committee of LICOBAG.</p>
